// called onclick of checkbox
function toggleSub(box, id) {    
    //get reference of checkbox
    var el = document.getElementById(id);
    if (box.checked) {
        el.style.display = 'block';
    } else {
        el.style.display = 'none';
    }
};


// called onclick of checkbox
function toggleMain(box, id) {    
    //get reference of checkbox
    var el = document.getElementById(id);
    if (box.checked) {
        el.style.display = 'none';
    } else {
        el.style.display = 'block';
    }
};